function toggleNav() {
    var updateElement = document.getElementById("menu-icon");
 //toggle adds a class if it's not there or removes it if it is.
 updateElement.classList.toggle("open");
}
function toggleWrap() {
    var updateElement = document.getElementById("wrapper");
 //toggle adds a class if it's not there or removes it if it is.
 updateElement.classList.toggle("open");
}       
